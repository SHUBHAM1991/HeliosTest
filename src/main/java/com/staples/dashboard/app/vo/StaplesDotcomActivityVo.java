public class StaplesDotcomActivityVo {

	private String skuNum;
	private String skuName;
	private String thumbnail;
	private String unitPrice;
	private String callReasonCart;
	private String cartActivity;
	private String purchaseHistory;
	private String lastBrowseDate;
	private String skuCategory;
	
	public String getSkuNum() {
		return skuNum;
	}
	public void setSkuNum(String skuNum) {
		this.skuNum = skuNum;
	}
	public String getSkuName() {
		return skuName;
	}
	public void setSkuName(String skuName) {
		this.skuName = skuName;
	}
	public String getThumbnail() {
		return thumbnail;
	}
	public void setThumbnail(String thumbnail) {
		this.thumbnail = thumbnail;
	}
	public String getUnitPrice() {
		return unitPrice;
	}
	public void setUnitPrice(String unitPrice) {
		this.unitPrice = unitPrice;
	}
	public String getCallReasonCart() {
		return callReasonCart;
	}
	public void setCallReasonCart(String callReasonCart) {
		this.callReasonCart = callReasonCart;
	}
	public String getCartActivity() {
		return cartActivity;
	}
	public void setCartActivity(String cartActivity) {
		this.cartActivity = cartActivity;
	}
	public String getPurchaseHistory() {
		return purchaseHistory;
	}
	public void setPurchaseHistory(String purchaseHistory) {
		this.purchaseHistory = purchaseHistory;
	}
	public String getLastBrowseDate() {
		return lastBrowseDate;
	}
	public void setLastBrowseDate(String lastBrowseDate) {
		this.lastBrowseDate = lastBrowseDate;
	}
	public String getSkuCategory() {
		return skuCategory;
	}
	public void setSkuCategory(String skuCategory) {
		this.skuCategory = skuCategory;
	}
	
	
}
